     public static void viewProfile(String tk, JTable table) throws ClassNotFoundException {
        try {
            Connection conn = ConnectOracle.getConnecOracle();

            CallableStatement cstmt = conn.prepareCall("{call p_get_profile_u(?, ?)}");
            cstmt.setString(1, tk); 
            cstmt.registerOutParameter(2, OracleTypes.CURSOR); 


            cstmt.execute();

            ResultSet rs = (ResultSet)cstmt.getObject(2);

            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setRowCount(0); // Clear existing data
            String[] columnNames = {"Profile", "Resource Name", "Resource Type", "Limit"};
            model.setColumnIdentifiers(columnNames);

            // Populate the table model
            while (rs.next()) {
                String profile = rs.getString("PROFILE");
                String resourceName = rs.getString("RESOURCE_NAME");
                String resourceType = rs.getString("RESOURCE_TYPE");
                String limit = rs.getString("LIMIT");
                model.addRow(new Object[]{profile, resourceName, resourceType, limit});
            }

            // Clean up
            rs.close();
            cstmt.close();
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
